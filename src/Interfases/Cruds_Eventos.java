        txtfinal.setVisible(true);

        ObjectContainer base = Db4o.openFile(Inicio.direccion);

        String nombre = " ", tipo = " ", descripcion = " ", patrocinador = " ", horain = " ", horaf = " ";
        Date fechai = null, fechaf = null;
        byte[] foto1;
        int ed = 0;
