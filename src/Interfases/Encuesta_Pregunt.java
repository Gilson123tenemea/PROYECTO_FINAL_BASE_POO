    public Evento obtenerEvento(String tipo) {
        ObjectContainer base = Db4o.openFile(Inicio.direccion);
        Query query = base.query();
        query.constrain(Evento.class);
        query.descend("tipo").constrain(tipo);
        ObjectSet<Evento> result = query.execute();

        Evento evento = null;
        if (!result.isEmpty()) {
            evento = result.get(0); // Suponiendo que solo hay un evento con el tipo especificado
        }

        base.close(); // No olvides cerrar la base de datos cuando hayas terminado
        return evento;
    }

    public void obtenerPreguntas(Evento evento) {
        ObjectContainer base = Db4o.openFile(Inicio.direccion);
        Query query = base.query();
        query.constrain(Encuesta.class);
        query.descend("evento").constrain(evento.getNombre()); // Filtra por el nombre del evento
        ObjectSet<Encuesta> result = query.execute();

        if (!result.isEmpty()) {
            for (Encuesta encuesta : result) {
                pregunta1 = encuesta.getPregunta1();
                pregunta2 = encuesta.getPregunta2();
                pregunta3 = encuesta.getPregunta3();
                pregunta4 = encuesta.getPregunta4();
                pregunta5 = encuesta.getPregunta5();

                // Haz algo con las preguntas (asignarlas a los TextView, por ejemplo)
                txt1.setText(pregunta1);
                txt2.setText(pregunta2);
                txt3.setText(pregunta3);
                txt4.setText(pregunta4);
                txt5.setText(pregunta5);
            }
        }

        base.close(); // No olvides cerrar la base de datos cuando hayas terminado
    }

