    public void crearPublico(ObjectContainer base) {
        try {
            // Obtener el último código
            ObjectSet<Publico_p> result = base.queryByExample(new Publico_p());
            int ultimoCodigo = result.size() + 1;

            // Formatear el código con ceros a la izquierda
            String cod = String.format("PUB-%03d", ultimoCodigo);
            lblcod.setText(cod);

            String seleccion = null;
            Date nacimiento = null;

            // establecer formato
            Date fecha = jDateFechaNaci.getDate();

            if (fecha != null) {
                seleccion = new SimpleDateFormat("dd/MM/yyyy").format(fecha);
            } else {
                System.out.println("Fecha no seleccionada");
            }

            // convertir el String seleccionado a Date
            SimpleDateFormat format = new SimpleDateFormat("dd/MM/yyyy");
            try {
                nacimiento = format.parse(seleccion);
                System.out.println("Date: " + nacimiento);

            } catch (ParseException e) {
                e.printStackTrace();
            }

            String sexo = rbnFemenino.isSelected() ? "Femenino" : (rbnMasculino.isSelected() ? "Masculino" : "");

            // Incremental code
            // Create a Publico_p instance with the cedula for checking existence
            Publico_p examplePublico = new Publico_p(null, null, null, null, null, null, txtCedula.getText().trim(), null, null, null, null, null, null, null, null);

            // Query the database to check for existing records
            ObjectSet<Publico_p> existingRecords = base.queryByExample(examplePublico);

            if (!existingRecords.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Ya existe un Publico con la cédula ingresada.", "Error", JOptionPane.ERROR_MESSAGE);
                lblcedula.setText("Campo obligatorio");
                return;
            }

            Validar();
            
            String codigoEvento = obtenerCodigoEventoSeleccionado();
            
            
//            public Publico_p(String codigo_publico, String preferencias_p, String contraseña, String codigo_encuesta, String codigo_cali_even, String cedula_persona, String cedula, String nombre, String apellido, String telefono, String correo, String direccion, String celular, Date fecchaNaci, String genero) {
            Publico_p mipublico = new Publico_p(cod, txtPreferencias.getText().trim(), jPasswordPublico.getText().trim(), null, codigoEvento, null, txtCedula.getText().trim(), txtNombre.getText().trim(), txtApellido.getText().trim(), txtTelefono.getText().trim(), txtEmail.getText().trim(), txtDireccion.getText().trim(),
                    txtCelular.getText().trim(), nacimiento, sexo);

            base.store(mipublico);

            JOptionPane.showMessageDialog(null, " Se guardo los datos de forma correcta");
        } finally {
            base.close();

        }

        // spnedad.setValue(0);
        txtEmail.setText("".trim());
        jDateFechaNaci.setDate(null);
        txtCedula.setText("".trim());
        txtNombre.setText("".trim());
        txtApellido.setText("".trim());
        txtCelular.setText("".trim());
        txtTelefono.setText("".trim());
        lblcod.setText(" ".trim());
        txtDireccion.setText("".trim());
        jPasswordPublico.setText("");
        txtPreferencias.setText("");
        rbnFemenino.setSelected(false);
        rbnMasculino.setSelected(false);

    }
    
    private String obtenerCodigoEventoSeleccionado() {
        String eventoSeleccionado = jComboBox1.getSelectedItem().toString();

        // Asumiendo que el código del evento está al principio del string antes del espacio
        String[] partes = eventoSeleccionado.split(" ");

        if (partes.length > 0) {
            return partes[0];
        } else {
            return "";  // Ajusta esto según la estructura real de tu ComboBox
        }
    }
    
    
    public void cargar() {
        ObjectContainer base = Db4o.openFile(Inicio.direccion);

        try {
            jComboBox1.removeAllItems();
            Query query = base.query();
            query.constrain(Evento.class);

            ObjectSet<Evento> eventos = query.execute();

            jComboBox1.addItem("Seleccione");
            while (eventos.hasNext()) {
                Evento tipoEvento = eventos.next();
                jComboBox1.addItem(tipoEvento.toString());
            }
        } finally {
            base.close();
        }
    }

